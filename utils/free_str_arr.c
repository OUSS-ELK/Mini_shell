#include "minishell.h"

void	free_2d(char ***arr)
{
	int	i;

	i = 0;
	if (!arr || !(*arr))
		return ;
	while ((*arr)[i])
	{
		free((*arr)[i]);
		i++;
	}
	free((*arr));
	(*arr) = NULL;
}
